package main

import "fmt"

func main() {

	// Creating Constant

	//Syntax:
	// const variable_name type = value

	// Creating a constant float32 type variable

	//Defining constant pi as float32 with 3.14159 value
	const pi float32 = 3.14159
	fmt.Println(pi)

	//Creating a constant string type variable
	const error_msg string = "Error Message"
	fmt.Println(error_msg)

}
